export function getProgramPageHref(slug: string) {
  return `/programs/${slug}`;
}

export function getShowcaseProgramBySlug(slug: string) {
  return showcasePrograms.find((program) => program.slug === slug) ?? null;
}

export function getRelatedShowcasePrograms(program: ShowcaseProgram, limit = 3) {
  return showcasePrograms
    .filter((item) => item.slug !== program.slug && item.category === program.category)
    .slice(0, limit);
}
