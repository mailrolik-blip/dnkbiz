async function syncCourseLessons(courseId, lessons) {
  const lessonSlugs = lessons.map((lesson) => lesson.slug);

  const obsoleteLessons = await prisma.lesson.findMany({
    where: {
      courseId,
      slug: {
        notIn: lessonSlugs,
      },
    },
    select: {
      id: true,
    },
  });

  if (obsoleteLessons.length > 0) {
    const obsoleteLessonIds = obsoleteLessons.map((lesson) => lesson.id);

    await prisma.lessonProgress.deleteMany({
      where: {
        lessonId: {
          in: obsoleteLessonIds,
        },
      },
    });

    await prisma.lesson.deleteMany({
      where: {
        id: {
          in: obsoleteLessonIds,
        },
      },
    });
  }

  for (const lesson of lessons) {
    await prisma.lesson.upsert({
      where: {
        courseId_slug: {
          courseId,
          slug: lesson.slug,
        },
      },
      update: {
        title: lesson.title,
        description: lesson.description,
        content: lesson.content,
        videoUrl: lesson.videoUrl,
        videoProvider: lesson.videoProvider,
        homeworkTitle: lesson.homeworkTitle,
        homeworkPrompt: lesson.homeworkPrompt,
        homeworkType: lesson.homeworkType,
        homeworkOptions: lesson.homeworkOptions,
        position: lesson.position,
        isPreview: lesson.isPreview ?? false,
        isPublished: true,
      },
      create: {
        courseId,
        title: lesson.title,
        slug: lesson.slug,
        description: lesson.description,
        content: lesson.content,
        videoUrl: lesson.videoUrl,
        videoProvider: lesson.videoProvider,
        homeworkTitle: lesson.homeworkTitle,
        homeworkPrompt: lesson.homeworkPrompt,
        homeworkType: lesson.homeworkType,
        homeworkOptions: lesson.homeworkOptions,
        position: lesson.position,
        isPreview: lesson.isPreview ?? false,
        isPublished: true,
      },
    });
  }
}

